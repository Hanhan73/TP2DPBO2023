    public void updateData(){
        String nama = txNama.getText();
        int stat = Integer.parseInt(txStat.getText());
        String weaponType = txWeaponType.getText();
        
        if(nama.isEmpty() && stat == 0 && weaponType.isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Tidak Boleh Ada Yang Kosong");
            
        } else {
            String sql = "UPDATE `weapon` SET weapon_type = '"+weaponType+"', nama = '"+nama+"', stat = "+stat+", foto_weap = '"+imgName+"' WHERE id = "+ID+"";
            c.updateQuery(sql);
            JOptionPane.showMessageDialog(this, "Data updated successfully.");

        }
        
    }
    
    public void readData() throws SQLException{
        System.out.println(ID);
        String sql = "SELECT * FROM `weapon` WHERE id="+ID+"";
        ResultSet r = c.selectQuery(sql);
        while(r.next()){
            txWeaponType.setText(r.getString("weapon_type"));
            txNama.setText(r.getString("nama"));
            txStat.setText(r.getString("stat"));
                
        }
    }
    

    
    private void btnUpload1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpload1ActionPerformed
        // TODO add your handling code here:
        String filename;
        try{
            JFileChooser chooser = new JFileChooser();
            FileNameExtensionFilter filter = new FileNameExtensionFilter("4 supported extensions", "jpg", "jpeg", "gif","png");
            chooser.setFileFilter(filter);
            chooser.showOpenDialog(null);
            File f = chooser.getSelectedFile();
            System.out.println(f.toString());
            ImageIcon icon = new ImageIcon(f.toString());
            Image image = null;
            image = icon.getImage().getScaledInstance(lbImage.getWidth(),lbImage.getHeight(),image.SCALE_SMOOTH);
            ImageIcon ic = new ImageIcon(image);
            lbImage.setIcon(ic);
            filename = f.getAbsolutePath();

            String newpath = "src/res/Images/";
            File directory = new File(newpath);
            if(!directory.exists()){
                directory.mkdir();
            }
            File sourceFile = null;
            File destinationFile = null;
            String extension = filename.substring(filename.lastIndexOf('.')+1);
            sourceFile = new File(filename);
            Date tanggal_update = new Date();
            String tampilan="yyyyMMddhhmmss";
            SimpleDateFormat fm= new SimpleDateFormat(tampilan);
            String tanggal = String.valueOf(fm.format(tanggal_update));
            destinationFile =  new File(newpath+tanggal.toString()+txNama.getText()+"."+ extension);
            
            imgName = tanggal.toString()+txNama.getText()+"."+ extension;
            Files.copy(sourceFile.toPath(),destinationFile.toPath());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error Upload" + e);
        }
    }//GEN-LAST:event_btnUpload1ActionPerformed

