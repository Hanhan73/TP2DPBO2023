    public void setPanelChara(){
        
        mainPanel.removeAll();
        String sql = "SELECT * FROM `character`";
        ResultSet r = c.selectQuery(sql);
        String nama, vision, weaponType,img;
        try{
            mainPanel.setLayout(new GridLayout(3,1));
            while(r.next()){
                id = r.getInt("id");
                nama = r.getString("nama");
                vision = r.getString("vision");
                weaponType = r.getString("weapon_type");
                img = r.getString("foto_char");
                mainPanel.add(new Card(new Character(id, nama, vision, weaponType, img)));
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
        }
        mainPanel.revalidate();
        mainPanel.repaint();
    
    }
    
    public int getId(){
        return id;
    }
    
    
